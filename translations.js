const languageButtons = document.querySelectorAll('[data-lang]');
const translationNodes = document.querySelectorAll('[data-i18n], [data-i18n-html]');

function resolveTranslation(source, key) {
  return key.split('.').reduce((value, part) => (value && value[part] !== undefined ? value[part] : null), source);
}

function setLang(lang) {
  const selectedLang = translations[lang] ? lang : 'pt';
  const locale = translations[selectedLang];
  document.documentElement.lang = selectedLang;
  translationNodes.forEach(node => {
    const key = node.dataset.i18n || node.dataset.i18nHtml;
    const translation = resolveTranslation(locale, key);
    if (translation !== null) {
      if (node.hasAttribute('data-i18n-html')) {
        node.innerHTML = translation;
      } else {
        node.textContent = translation;
      }
    }
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(node => {
    const key = node.dataset.i18nAriaLabel;
    const translation = resolveTranslation(locale, key);
    if (translation !== null) {
      node.setAttribute('aria-label', translation);
    }
  });
  const marqueeTrack = document.getElementById('marquee-track');
  const marqueeItems = resolveTranslation(locale, 'marquee.items');
  if (marqueeTrack && Array.isArray(marqueeItems)) {
    const loopItems = [...marqueeItems, ...marqueeItems];
    marqueeTrack.innerHTML = loopItems
      .map(item => `<span class="marquee-item">${item} <span class="sep">◆</span></span>`)
      .join('');
  }
  languageButtons.forEach(btn => btn.classList.toggle('active', btn.dataset.lang === selectedLang));
  localStorage.setItem('garisign-lang', selectedLang);
}

languageButtons.forEach(btn => btn.addEventListener('click', () => setLang(btn.dataset.lang)));

const savedLang = localStorage.getItem('garisign-lang') || 'pt';
setLang(savedLang);
